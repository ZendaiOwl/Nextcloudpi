# Prints a line using printf instead of using echo
# For compatibility and reducing unwanted behaviour
function Print () {
    printf '%s\n' "$@"
}

function configure () {
    local GW DNS IFACE
    GW="$( ip r | grep "default via"   | awk '{ print $3 }' | head -1 )"
    DNS="$( grep nameserver /etc/resolv.conf | head -1 | awk '{ print $2 }' )"
    [[ "$DNS" == "" ]] && DNS="$GW"
    IFACE="$( ip r | grep "default via" | awk '{ print $5 }' | head -1 )"
    [[ "$IFACE" == "" ]] && { Print "Couldn't find default interface"; exit 1; }
    
    ## DHCPCD
    if [[ -f '/etc/dhcpcd.conf' ]]
    then # delete NCP config
         grep -q "^# NextcloudPi autogenerated" /etc/dhcpcd.conf && \
         sed -i '/^# NextcloudPi autogenerated/,+6d' /etc/dhcpcd.conf
         [[ "$ACTIVE" != "yes" ]] && {
             systemctl restart dhcpcd
             Print "Enabled: DHCP"
             return
         }
         cat >> /etc/dhcpcd.conf <<EOF
# NextcloudPi autogenerated
# Do not modify! Instead use ncp-config
interface $IFACE
static ip_address=$IP/24
static routers=$GW
static domain_name_servers=$DNS

# Local loopback
auto lo
iface lo inet loopback
EOF
         systemctl restart dhcpcd
    else ## NETWORK MANAGER
         cp -n /etc/network/interfaces /etc/network/interfaces-ncp-backup-orig
         if [[ "$ACTIVE" != "yes" ]]
         then cat > /etc/network/interfaces <<EOF
# Wired adapter #1
allow-hotplug $IFACE
no-auto-down $IFACE
auto $IFACE
iface $IFACE inet dhcp

# Local loopback
auto lo
iface lo inet loopback
EOF
              ifdown "$IFACE"
              ifup "$IFACE"
              Print "Enabled: DHCP"
              return
         fi 
         cat > /etc/network/interfaces <<EOF
# Generated by: ncp-config
source /etc/network/interfaces.d/*

# Local loopback
auto lo
iface lo inet loopback

# Interface $IFACE
auto $IFACE
allow-hotplug $IFACE
iface $IFACE inet static
    address $IP
    netmask 255.255.255.0
    gateway $GW
    dns-nameservers $DNS 8.8.8.8
EOF
        pkill dhclient
        ifdown "$IFACE"
        ifup "$IFACE"
    fi
    ncc config:system:set trusted_domains 1 --value="$IP"
    set-nc-domain "$IP"
    Print "Static IP set to: $IP"
}

